package day6.Loops;

public class Example6 {
	//do while syntax

	public static void main(String[] args) {
		int i=1;
		do{
			System.out.println("Number is "+i);
			i++;
		}while(i<5);
		System.out.println("********Using while*************");
		while(i<5){
			System.out.println("Number with while "+i);
		}

	}

}
